def generate_fleet_config(session: Any) -> str:
    """Generate fleet.yaml additions for the capability.

    Args:
        session: BuilderSession with completed capability definition.

    Returns:
        YAML configuration string.
    """
    capability_name = session.name or "capability"

    # Build worker slot overrides section
    overrides = ""
    if session.worker_slots:
        for slot, model in session.worker_slots.items():
            overrides += f"""
  {slot}:
    model: {model}
"""

    yaml = f"""# Generated capability registration for: {capability_name}
# Created: {datetime.utcnow().isoformat()}
# Builder Session: {session.session_id}

capabilities:
  {capability_name}:
    enabled: true
    description: "{session.description or ''}"
    intent_patterns:
"""

    # Add intent patterns
    for pattern in session.intent_patterns:
        yaml += f'      - "{pattern}"\n'

    yaml += "\n"

    # Add worker slot overrides if present
    if overrides:
        yaml += f"""    worker_overrides:{overrides}"""

    yaml += f"""
    output_schema:
      type: object
      required:
"""

    # Add required fields
    if session.output_schema and "required" in session.output_schema:
        for field in session.output_schema["required"]:
            yaml += f'        - "{field}"\n'

    yaml += """
# NOTE: To integrate this configuration:
# 1. Add the above YAML to your fleet.yaml under the 'capabilities' section
# 2. Reload or restart Agentend to register the new capability
# 3. The capability will become available for intent routing
"""

    return yaml


def _python_list_literal(items: list) -> str:
    """Convert Python list to a literal string representation.

    Args:
        items: List to convert.

    Returns:
        String like "[1, 2, 3]".
    """
    if not items:
        return "[]"

    # Handle list of dicts vs strings
    if items and isinstance(items[0], dict):
        dicts_str = ",\n        ".join(json.dumps(item) for item in items)
        return f"[\n        {dicts_str}\n    ]"
    else:
        items_str = ", ".join(json.dumps(item) for item in items)
        return f"[{items_str}]"


def _python_dict_literal(data: dict) -> str:
    """Convert Python dict to a literal string representation.

    Args:
        data: Dict to convert.

    Returns:
        String like "{'key': 'value'}".
    """
    if not data:
        return "{}"

    return json.dumps(data, indent=8)


def _escape_string(s: str) -> str:
    """Escape a string for Python source code.

    Args:
        s: String to escape.

    Returns:
        Escaped string.
    """
    return s.replace('"', '\\"').replace("\n", "\\n")


def _escape_docstring(s: str) -> str:
    """Escape a string for use in a Python docstring.

    Args:
        s: String to escape.

    Returns:
        Escaped string.
    """
    return s.replace('"""', '\\"""').replace("\n", "\\n")


def _to_class_name(name: str) -> str:
    """Convert snake_case name to PascalCase class name.

    Args:
        name: Name to convert (e.g., 'ticket_classifier').

    Returns:
        Class name (e.g., 'TicketClassifier').
    """
    parts = name.split("_")
    return "".join(part.capitalize() for part in parts)


